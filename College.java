public class College {

    private String name;
    private double avgGPA;
    private int satMin;
    private int satMax;
    private int actMin;
    private int actMax;
    private int writingPoints;
    private int acceptanceRate;

    public String getName() {
        return name;
    }

    public double getAvgGPA() {
        return avgGPA;
    }

    public int getSatMin() {
        return satMin;
    }

    public int getSatMax() {
        return satMax;
    }

    public int getActMin() {
        return actMin;
    }

    public int getActMax() {
        return actMax;
    }

    public int getWritingPoints() {
        return writingPoints;
    }

    public int getAcceptanceRate() {
        return acceptanceRate;
    }

}